package corejava;

public class BitwiseOperators {

	public static void main(String[] args) {
		int a=10;
		int b=11;
		int c=9;
		System.out.println("a&b:"+(a&b));
		System.out.println("a|b:"+(a|b));
		System.out.println("a^b:"+(a^b));
		System.out.println("~a:"+(~c));
		
		int x=20;
		int y=45;
		System.out.println("x<<3:"+(x<<3));
		System.out.println("x>>3:"+(x>>3));
		System.out.println("y<<2:"+(y<<2));
	}

}
