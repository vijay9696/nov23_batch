package corejava;

public class ForLoopExample {

	public static void main(String[] args) {
		int i;
		for(i=0;i<10;i++)
		{
			System.out.println("i:"+i);
		}
		System.out.println("Exit when i="+i);
		
		int j;
		for(j=10;j>=1;j--)
		{
			System.out.println("j:"+j);
		}
		System.out.println("Exit when j="+j);
	}

}
